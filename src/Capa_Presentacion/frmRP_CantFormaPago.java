    private void btnMostrarReporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarReporteActionPerformed
        System.out.println(jcFechaIni.getDate());
        try {
            Date fechaIni = jcFechaIni.getDate();
            Date fechaFin = jcFechaFin.getDate();

            if (fechaIni == null || fechaFin == null) {
                JOptionPane.showMessageDialog(null, "Debe seleccionar ambas fechas.");
                return;
            }

            if (fechaFin.before(fechaIni)) {
                JOptionPane.showMessageDialog(null, "La fecha final debe ser mayor o igual que la fecha inicial.");
                return;
            }
            Container contenedor = this.vistareporte;
            contenedor.setLayout(new BorderLayout());
            contenedor.removeAll();
            Map parametros = new HashMap();
            parametros.put("fecha_ini", jcFechaIni.getDate());
            parametros.put("fecha_fin", jcFechaFin.getDate());
            parametros.put("REPORT_DIR", "src/Reportes/");
            JRViewer objReporte = new clsReportes().reporteInterno("MedioDePago.jasper", parametros);
            contenedor.add(objReporte);
            contenedor.revalidate();
            contenedor.repaint();
            this.vistareporte.setVisible(true);
        } catch (Exception ex) {
            System.getLogger(frmRP_ServiciosLucrativos.class.getName()).log(System.Logger.Level.ERROR, (String) null, ex);
        }
    }//GEN-LAST:event_btnMostrarReporteActionPerformed

