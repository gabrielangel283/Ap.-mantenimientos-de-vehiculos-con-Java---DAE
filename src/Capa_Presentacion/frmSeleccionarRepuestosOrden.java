    private void listarRepuestos() {
        DefaultTableModel modelo = new DefaultTableModel();
        ResultSet rsR = null;
        modelo.addColumn("Repuesto");
        modelo.addColumn("Precio");
        modelo.addColumn("Stock");
        modelo.addColumn("Estado");
        modelo.addColumn("Marca");
        modelo.addColumn("Descripción");
        try {
            rsR = objRepuesto.listarRepuestosListos();
            while (rsR.next()) {
                modelo.addRow(new Object[]{rsR.getString("nombre_repuesto"), rsR.getFloat("precio"), rsR.getInt("stock"),
                    rsR.getString("estado"), rsR.getString("nombre_marca"), rsR.getString("descripcion")});
            }
            tblRepuestos.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Error al listar repuestos: " + e.getMessage());
        }
    }

    private void listarRepuestosFiltro(String filtro, String cont) {
        DefaultTableModel modelo = new DefaultTableModel();
        ResultSet rsR = null;
        modelo.addColumn("Repuesto");
        modelo.addColumn("Precio");
        modelo.addColumn("Stock");
        modelo.addColumn("Estado");
        modelo.addColumn("Marca");
        modelo.addColumn("Descripción");
        try {
            if (filtro == "Nombre") {
                rsR = objRepuesto.buscarRepuesto_Nombre(cont);
                while (rsR.next()) {
                    modelo.addRow(new Object[]{rsR.getString("nombre_repuesto"), rsR.getFloat("precio"), rsR.getInt("stock"),
                        rsR.getString("estado"), rsR.getString("nombre_marca"), rsR.getString("descripcion")});
                }
                tblRepuestos.setModel(modelo);
            } else if (filtro == "Marca") {
                rsR = objRepuesto.buscarRepuesto_Marca(cont);
                while (rsR.next()) {
                    modelo.addRow(new Object[]{rsR.getString("nombre_repuesto"), rsR.getFloat("precio"), rsR.getInt("stock"),
                        rsR.getString("estado"), rsR.getString("nombre_marca"), rsR.getString("descripcion")});
                }
                tblRepuestos.setModel(modelo);
            } else {
                rsR = objRepuesto.listarRepuestosListos();
                while (rsR.next()) {
                    modelo.addRow(new Object[]{rsR.getString("nombre_repuesto"), rsR.getFloat("precio"), rsR.getInt("stock"),
                        rsR.getString("estado"), rsR.getString("nombre_marca"), rsR.getString("descripcion")});
                }
                tblRepuestos.setModel(modelo);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Error al listar repuestos: " + e.getMessage());
        }
    }

