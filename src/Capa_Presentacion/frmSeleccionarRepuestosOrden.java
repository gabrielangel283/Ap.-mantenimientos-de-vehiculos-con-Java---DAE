    private void txtFiltroKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFiltroKeyReleased
        String cont = txtFiltro.getText().trim();

        try {
            if (cont.trim().equals("")) {
                listarRepuestos();
            } else {
                listarRepuestosFiltro(cboFiltros.getSelectedItem().toString(), cont);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_txtFiltroKeyReleased

    private void tblRepuestosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblRepuestosMouseClicked
        int fila = tblRepuestos.getSelectedRow();
        try {
            Double precio = Double.parseDouble(tblRepuestos.getValueAt(fila, 1).toString());
            txtPrecio.setText(precio.toString());
            ResultSet rsRep = objRepuesto.buscarRepuestoEstricto(tblRepuestos.getValueAt(fila, 0).toString());
            if(rsRep.next()){
                rep = rsRep.getInt("repuesto_id");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_tblRepuestosMouseClicked

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        int fila = tblRepuestos.getSelectedRow();
        if (fila != -1) {
            int cant = (int) spnStock.getValue();
            if (cant <= Integer.parseInt(tblRepuestos.getValueAt(fila, 2).toString())) {
                pasarDatos();
            } else {
                JOptionPane.showMessageDialog(rootPane, "La cantidad supera el stock");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Debe seleccionar un repuesto");
        }
    }//GEN-LAST:event_btnAgregarActionPerformed


