    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try {
            // 1. Validar selección
            if (cboEstado.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(this, "Debe seleccionar un estado.", "Aviso", JOptionPane.WARNING_MESSAGE);
                return;
            }

            // 2. Obtener el texto del combo
            String estadoSeleccionado = cboEstado.getSelectedItem().toString();

            // 3. Preparar contenedor
            Container contenedor = this.vistareporte;
            contenedor.removeAll();
            contenedor.setLayout(new BorderLayout());

            // 4. Parámetros
            Map<String, Object> parametros = new HashMap<>();
            parametros.put("p_estado", estadoSeleccionado);
            parametros.put("REPORT_DIR", "src/Reportes/");

            // 5. Llamar al reporte
            // Asegúrate que el archivo .jasper se llame ReporteDemoras.jasper
            JRViewer objReporte = new clsReportes().reporteInterno("ReporteDemoras.jasper", parametros);

            if (objReporte != null) {
                contenedor.add(objReporte);
                contenedor.revalidate();
                contenedor.repaint();
                this.vistareporte.setVisible(true);
            } else {
                 JOptionPane.showMessageDialog(this, "No se encontraron registros para el estado: " + estadoSeleccionado);
            }

        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error crítico: " + ex.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed


