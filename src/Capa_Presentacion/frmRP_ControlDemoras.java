    private void cargarEstadosDesdeBD() {
        DefaultComboBoxModel modelo = new DefaultComboBoxModel();
        modelo.addElement("-- Seleccione --");
        
        try {
            String sql = "SELECT DISTINCT estado FROM ORDEN ORDER BY estado"; 
            ResultSet rs = objConexion.consultarBD(sql);
            
            while(rs.next()){
                modelo.addElement(rs.getString("estado"));
            }
            
            cboEstado.setModel(modelo);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar estados: " + e.getMessage());
        }
    }
    
