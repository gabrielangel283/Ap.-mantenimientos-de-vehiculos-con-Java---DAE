    private void btnDarBajaAltaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDarBajaAltaActionPerformed
        int filaSelec = tblMarcas.getSelectedRow();
        if (filaSelec != -1) {
            int marca_id = (int) tblMarcas.getValueAt(filaSelec, 0);
            try {
                Marca.darBajaAltaMarca(marca_id);
                JOptionPane.showMessageDialog(this, "Cambio de estado de la marca exitoso");
                mostrarMarcasTabla();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "ERROR", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Debe elegir una marca para dar de alta o baja");
        }
    }//GEN-LAST:event_btnDarBajaAltaActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int filaSelec = tblMarcas.getSelectedRow();
        if (filaSelec != -1) {
            int marca_id = (int) tblMarcas.getValueAt(filaSelec, 0);
            try {
                int a = JOptionPane.showConfirmDialog(this, "¿Desea eliminar o poner la marca en inactivo?");
                if (a == 0) {
                    int rpta = Marca.eliminarMarca(marca_id);
                    if (rpta == 1) {
                        JOptionPane.showMessageDialog(this, "Se actualizo el estado del usuario a inactivo", "ELIMINAR MARCA", JOptionPane.YES_NO_OPTION);
                    } else {
                        JOptionPane.showMessageDialog(this, "Se elimino la marca seleccionada");
                    }
                } else {
                    return;
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            } finally {
                mostrarMarcasTabla();
                mostrarCantMarcas();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Debe elegir una marca para eliminar");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed


