    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        if (btnNuevo.getText().equalsIgnoreCase("Nuevo")) {
            habilitar(true);
            btnNuevo.setEnabled(true);
            btnNuevo.setText("Guardar");
        } else {
            if (txtNomMarca.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(this, "Debe ingreasar todos los campos a¿obligatorios", "Registrar marca", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            try {
                String nombreMarca = txtNomMarca.getText();
                String descripcionMarca = txtDescriocionMarca.getText();

                Marca.insertarMarca(nombreMarca, descripcionMarca);

                JOptionPane.showMessageDialog(this, "Nueva marca registrada exitosamente");

                habilitar(false);
                txtNomMarca.setText("");
                txtDescriocionMarca.setText("");
                btnNuevo.setText("Nuevo");
                mostrarMarcasTabla();
                mostrarCantMarcas();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "ERROR", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnNuevoActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String nombreMarca = txtBuscarMarca.getText();
        if (nombreMarca.trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Debe ingresar el nombre de la marca para la busqueda");
            return;
        }
        try {
            ResultSet rs = Marca.buscarMarcaPorNombre(nombreMarca);
            Util.llenarTabla(tblMarcas, rs);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "ERROR", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

