    private void cboTipoClienteItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboTipoClienteItemStateChanged
        String tipoC = cboTipoCliente.getSelectedItem().toString().toLowerCase();
        if (tipoC.equalsIgnoreCase("natural")) {
            lblNmDoc.setText("DNI: ");
            txtTipoComprob.setText("Boleta");
        } else {
            lblNmDoc.setText("RUC: ");
            txtTipoComprob.setText("Factura");
        }
        try {
            Util.llenarTabla(tblClientes, new Cliente().listarClientesPorTipo(tipoC));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_cboTipoClienteItemStateChanged


