    private void btnPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagarActionPerformed
        int filaSelect = tblOrdenes.getSelectedRow();
        String dniCl = (String) tblClientes.getValueAt(filaSelect, 0);
        if (filaSelect != -1) {
            int a = JOptionPane.showConfirmDialog(this, "¿Desea el cliente "+dniCl+" realizar el pago de la orden " + orden_id + "?", "PAGAR ORDEN", JOptionPane.YES_NO_OPTION);
            if (a == 0) {
                try {
                    String tipoComp = cboTipoCliente.getSelectedItem().toString();
                    float monto = Orden.obtenerMonto(orden_id);
                    int cliente_id = new Cliente().obtenerIdPorDNI(dniCl);
                    String metodoPago = cboMetodoPago.getSelectedItem().toString().toLowerCase();

                    new Orden().pagarOrden(orden_id, tipoComp, monto, metodoPago, cliente_id);

                    JOptionPane.showMessageDialog(rootPane, "Pago realizado correctamente");
                    inicial();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una orden, SELECCIONAR ORDEN");
        }
    }//GEN-LAST:event_btnPagarActionPerformed

    private void jcFechaInicioPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_jcFechaInicioPropertyChange
        try {
            // Verificar si la fecha no es nula
            if (jcFechaInicio.getDate() == null) {
                return; // Salir si no hay fecha seleccionada
            }

            // Validar que la fecha no sea posterior a hoy
            Date hoy = new Date();
            if (jcFechaInicio.getDate().after(hoy)) {
                JOptionPane.showMessageDialog(
                        this,
                        "La fecha inicial debe ser menor a la fecha actual",
                        "FECHA INVÁLIDA",
                        JOptionPane.INFORMATION_MESSAGE
                );
                jcFechaInicio.setDate(null); // Limpiar la fecha inválida
                return;
            }

            // Convertir la fecha a LocalDate
            LocalDate fecha_ini = jcFechaInicio.getDate()
                    .toInstant()
                    .atZone(ZoneId.systemDefault())
                    .toLocalDate();

            // Obtener y mostrar las órdenes filtradas
            ResultSet rs = Orden.listarOrdenesPorMayorAFecha_Estado("completado", fecha_ini);
            Util.llenarTabla(tblOrdenes, rs);

            // Habilitar el botón cuando hay resultados filtrados
            btnPagar.setEnabled(true);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                    this,
                    e.getMessage(),
                    "ERROR",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_jcFechaInicioPropertyChange

