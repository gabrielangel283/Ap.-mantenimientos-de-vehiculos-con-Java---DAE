    private void inicial() {
        DefaultTableModel model = (DefaultTableModel) tblClientes.getModel();

        // 2. Eliminar todas las filas del modelo
        int rowCount = model.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
            model.removeRow(i);
        }
        btnPagar.setEnabled(false);
        listarOrdenesSegunEstado("completado");
        jcFechaInicio.setDate(null);
        tblClientes.setEnabled(false);
    }

    private void listarOrdenesSegunEstado(String estado) {
        try {
            ResultSet rs = Orden.listarOrdenesPorEstado(estado);
            Util.llenarTabla(tblOrdenes, rs);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }

