    private void btnSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSiguienteActionPerformed
        int filaSelect = tblOrdenes.getSelectedRow();
        try {
            if (filaSelect != -1) {
                orden_id = (int) tblOrdenes.getValueAt(filaSelect, 0);
                btnSiguiente.setEnabled(false);
                btnCancelar.setEnabled(true);
                tblClientes.setEnabled(true);
                cboTipoCliente.setEnabled(true);
                cboMetodoPago.setEnabled(true);
                txtNumDoc.setEnabled(true);
                ResultSet rs = new Cliente().listarClientesPorTipo("natural");
                Util.llenarTabla(tblClientes, rs);
            } else {
                JOptionPane.showMessageDialog(this, "Debe elegir una orden para pagar", "SELECCIONAR ORDEN", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSiguienteActionPerformed

