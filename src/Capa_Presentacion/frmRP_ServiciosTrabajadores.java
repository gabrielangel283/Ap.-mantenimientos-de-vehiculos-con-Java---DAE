    private void btnMostrarReporteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarReporteActionPerformed
        try {
            if (jcFechaIni.getDate() == null || jcFechaFin.getDate() == null) {
                JOptionPane.showMessageDialog(this, "Debes agregar una fecha inicial", "AVISO", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            if(jcFechaIni.getDate().after(jcFechaFin.getDate())) {
                JOptionPane.showMessageDialog(this, "La fecha inicial debe ser menor a la fecha final", "AVISO", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            Date hoy = new Date(); // fecha actual
            if(jcFechaFin.getDate().after(hoy)) {
                JOptionPane.showMessageDialog(this, "La fecha final debe ser menor a la actual", "AVISO", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            Date fecha_inicio = jcFechaIni.getDate();
            Date fecha_final = jcFechaFin.getDate();
            mostrarReporte(fecha_inicio, fecha_final);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "ERROR", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnMostrarReporteActionPerformed


