    private void mostrarReporte(Date fecha_inicio, Date fecha_final) throws Exception {
        try {
            clsReportes objR = new clsReportes();
            Map<String, Object> parametros = new HashMap<>();

            // 1. Mantenemos los parámetros Date para las consultas SQL
            parametros.put("fecha_inicio", fecha_inicio);
            parametros.put("fecha_fin", fecha_final);

            // 2. CREAMOS LOS NUEVOS PARÁMETROS STRING PARA LOS TEXTFIELD
            // Usamos el formato que querías
            java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd/MM/yyyy");
            parametros.put("fecha_inicio_str", sdf.format(fecha_inicio));
            parametros.put("fecha_fin_str", sdf.format(fecha_final));

            // 3. Los parámetros de directorio y usuario
            parametros.put("REPORT_DIR", "src/Reportes/");
            parametros.put("SUBREPORT_DIR", "src/Reportes/");
            parametros.put("usuario", nombreUsu);

            // El resto de tu código sigue igual
            JRViewer visor = objR.obtenerVisorInterno("ServPorTrabajador", parametros);
            cont.setLayout(new BorderLayout());
            cont.removeAll();
            cont.add(visor, BorderLayout.CENTER);
            cont.revalidate();
            cont.repaint();

        } catch (Exception ex) {
            throw new Exception("Error al mostrar el reporte de servicios: " + ex.getMessage());
        }
    }

