    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        Vehiculo vh = new Vehiculo();

        try {
            String dni = txtDni.getText().trim();

            if (!dni.matches("^[0-9]{8}$")) {
                JOptionPane.showMessageDialog(null, "El DNI debe tener 8 dígitos.");
                return;
            }

            ResultSet rs1 = vh.buscarCliente(dni);
            if (!rs1.next()) {
                JOptionPane.showMessageDialog(null, "No existe un cliente con ese DNI.");
                return;
            }
            int cliente_id = rs1.getInt("cliente_id");

            String nombreMarca = cboMarca.getSelectedItem().toString().trim();
            ResultSet rs2 = vh.buscarIdMarca(nombreMarca);

            if (!rs2.next()) {
                JOptionPane.showMessageDialog(null, "La marca seleccionada no existe.");
                return;
            }
            int marca_id = rs2.getInt("marca_id");

            String modelo = txtModel.getText().trim();
            if (modelo.isEmpty()) {
                JOptionPane.showMessageDialog(null, "El modelo no puede estar vacío.");
                return;
            }

            String color = txtColor.getText().trim();
            if (color.length() > 30) {
                JOptionPane.showMessageDialog(null, "El color no debe exceder 30 caracteres.");
                return;
            }

            String placa = txtPlaca.getText().trim();

            if (!placa.matches("^[0-9A-Z]{6,8}$")) {
                JOptionPane.showMessageDialog(null,
                        "La placa debe tener entre 6 y 8 caracteres en mayúsculas (0-9, A-Z).");
                return;
            }

            // Verificar unicidad de placa
            if (vh.placaExiste(placa)) {
                JOptionPane.showMessageDialog(null, "La placa ingresada ya existe.");
                return;
            }

            String descripcion = txtDesc.getText().trim();

            vh.insertarVehiculo(modelo, placa, descripcion, color, cliente_id, marca_id);

            ResultSet rs3 = vh.listarVehiculo();
            listarVehiculo(rs3);
            limpiar();

        } catch (Exception e) {
            System.out.println("Error: " + e.getMessage());
        }

    }//GEN-LAST:event_btnSaveActionPerformed

