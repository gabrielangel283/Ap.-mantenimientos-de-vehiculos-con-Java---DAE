    private void listarVehiculo(ResultSet rs) {
        DefaultTableModel tm = new DefaultTableModel();

        String[] columns = {"vehiculo_id", "modelo", "color", "num_placa", "estado", "fecha_registro", "marca", "num_documento", "cliente"};
        for (int i = 0; i < columns.length; i++) {
            tm.addColumn(columns[i]);
        }

        try {
            while (rs.next()) {
                i++;
                Object[] row = new Object[columns.length];
                for (int j = 0; j < columns.length; j++) {
                    row[j] = rs.getString(columns[j]);
                    if (columns[j].equalsIgnoreCase("estado")) {
                        row[j] = rs.getString(columns[j]).equalsIgnoreCase("t") ? "Activo" : "Inactivo";
                    }
                }
                tm.addRow(row);
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }

        tblCars.setModel(tm);
    }

    private void listarMarca() {
        JComboBox<String> myCbo = new JComboBox<>();
        JComboBox<String> myCbo2 = new JComboBox<>();
        ResultSet rs = new Vehiculo().listarMarcas();

        myCbo2.addItem("Todos");

        try {
            while (rs.next()) {
                myCbo.addItem(rs.getString("nombre_marca"));
                myCbo2.addItem(rs.getString("nombre_marca"));
            }
        } catch (SQLException ex) {
            System.getLogger(ex.getMessage());
        }
        cboMarca.setModel(myCbo.getModel());
        cboFilterBrand.setModel(myCbo2.getModel());
    }

