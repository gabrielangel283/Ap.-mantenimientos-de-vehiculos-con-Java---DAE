    private void txtGeneralKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtGeneralKeyPressed
        if (debounceTimer != null && debounceTimer.isRunning()) {
            debounceTimer.stop();
        }

        debounceTimer = new Timer(300, (e) -> {
            String filter = txtGeneral.getText();
            String marca = cboFilterBrand.getSelectedItem().toString();

            ResultSet rs = new Vehiculo().listarPorFiltros(filter, marca);
            listarVehiculo(rs);
        });

        debounceTimer.setRepeats(false);
        debounceTimer.start();
    }//GEN-LAST:event_txtGeneralKeyPressed

