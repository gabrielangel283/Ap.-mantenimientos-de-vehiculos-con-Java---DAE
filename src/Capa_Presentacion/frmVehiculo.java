    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int filaSelec = tblCars.getSelectedRow();
        Vehiculo vh = new Vehiculo();

        if (filaSelec != -1) {

            int vehiculo_id = Integer.parseInt(tblCars.getValueAt(filaSelec, 0).toString());

            try {

                int opcion = JOptionPane.showConfirmDialog(
                        this,
                        "¿Estás seguro de modificar este vehículo?",
                        "Confirmar Modificación",
                        JOptionPane.YES_NO_OPTION,
                        JOptionPane.WARNING_MESSAGE
                );

                if (opcion != JOptionPane.YES_OPTION) {
                    return;
                }

                String dni = txtDni.getText().trim();

                if (!dni.matches("^[0-9]{8}$")) {
                    JOptionPane.showMessageDialog(this, "El DNI debe tener 8 dígitos.");
                    return;
                }

                ResultSet rs1 = vh.buscarCliente(dni);
                if (!rs1.next()) {
                    JOptionPane.showMessageDialog(this, "No existe un cliente con ese DNI.");
                    return;
                }
                int cliente_id = rs1.getInt("cliente_id");

                String nombreMarca = cboMarca.getSelectedItem().toString().trim();
                ResultSet rs2 = vh.buscarIdMarca(nombreMarca);

                if (!rs2.next()) {
                    JOptionPane.showMessageDialog(this, "La marca seleccionada no existe.");
                    return;
                }
                int marca_id = rs2.getInt("marca_id");

                String modelo = txtModel.getText().trim();
                if (modelo.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "El modelo no puede estar vacío.");
                    return;
                }

                String color = txtColor.getText().trim();
                if (color.length() > 30) {
                    JOptionPane.showMessageDialog(this, "El color no debe exceder 30 caracteres.");
                    return;
                }

                String placa = txtPlaca.getText().trim();

                if (!placa.matches("^[0-9A-Z]{6,8}$")) {
                    JOptionPane.showMessageDialog(this,
                            "La placa debe tener entre 6 y 8 caracteres en mayúsculas (0-9, A-Z).");
                    return;
                }

                if (vh.placaExisteParaOtroVehiculo(placa, vehiculo_id)) {
                    JOptionPane.showMessageDialog(this, "La placa ingresada ya está asociada a otro vehículo.");
                    return;
                }

                String descripcion = txtDesc.getText().trim();

                int rpta = vh.actualizarVehiculo(
                        vehiculo_id,
                        modelo,
                        placa,
                        descripcion,
                        color,
                        cliente_id,
                        marca_id
                );

                if (rpta == 1) {
                    ResultSet rs = new Vehiculo().listarVehiculo();
                    listarVehiculo(rs);
                    limpiar();
                    JOptionPane.showMessageDialog(this, "Vehículo modificado correctamente.");
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            }

        } else {
            JOptionPane.showMessageDialog(this, "Debe elegir un vehículo para modificar.");
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

