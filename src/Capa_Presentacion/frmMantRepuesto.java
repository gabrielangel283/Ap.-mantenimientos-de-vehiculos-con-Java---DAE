    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        if (btnNuevo.getText().equals("Nuevo")) {
            habilitarControles(true);
            limpiarControles();
            btnNuevo.setText("Guardar");
            btnModificar.setEnabled(false); // Bloqueo modificar
            txtNombre.requestFocus();
        } else { // Guardar Nuevo
            try {
                if (txtNombre.getText().isEmpty() || txtPrecio.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Complete Nombre y Precio.");
                    return;
                }

                String nombre = txtNombre.getText();
                double precio = Double.parseDouble(txtPrecio.getText());
                // Si está agotado, stock es 0 aunque el campo esté vacío
                int stock = txtStock.getText().isEmpty() ? 0 : Integer.parseInt(txtStock.getText());
                String descripcion = txtDescripcion.getText();
                String estado = chkEstado.getSelectedItem().toString();
                String nombreMarca = cboMarca.getSelectedItem().toString();

                int idMarca = objRepuesto.obtenerIdMarca(nombreMarca);

                objRepuesto.registrarRepuesto(nombre, precio, stock, descripcion, estado, idMarca);

                JOptionPane.showMessageDialog(this, "Repuesto registrado correctamente.");
                listarRepuestos();
                habilitarControles(false);
                limpiarControles();

            } catch (NumberFormatException nfe) {
                JOptionPane.showMessageDialog(this, "Precio y Stock deben ser números.");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_btnNuevoActionPerformed

    private void btnDarBajaAltaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDarBajaAltaActionPerformed
        if (idRepuestoSeleccionado == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un repuesto.");
            return;
        }
        try {
            String estadoActual = chkEstado.getSelectedItem().toString();
            Object[] opciones = {"disponible", "agotado", "descontinuado"};
            String seleccion = (String) JOptionPane.showInputDialog(this,
                    "Cambiar estado:", "Estado", JOptionPane.QUESTION_MESSAGE, null, opciones, estadoActual);

            if (seleccion != null) {
                objRepuesto.darBajaAlta(idRepuestoSeleccionado, seleccion);
                listarRepuestos();
                limpiarControles();
                JOptionPane.showMessageDialog(this, "Estado actualizado.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnDarBajaAltaActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (idRepuestoSeleccionado == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un repuesto.");
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(this, "¿Eliminar repuesto?", "Confirmar", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            try {
                objRepuesto.eliminarRepuesto(idRepuestoSeleccionado);
                listarRepuestos();
                limpiarControles();
                JOptionPane.showMessageDialog(this, "Repuesto eliminado.");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        if (idRepuestoSeleccionado == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un repuesto de la tabla.");
            return;
        }

        // 3. Lógica del Botón Modificar (Cambia texto y bloquea Nuevo)
        if (btnModificar.getText().equals("Modificar")) {
            habilitarControles(true);
            btnNuevo.setEnabled(false); // Bloquear nuevo
            btnModificar.setText("Guardar Cambios"); // Cambio de nombre

            // Verificar si al habilitar debe bloquearse el stock por ser agotado
            chkEstadoActionPerformed(null);

        } else { // Guardar Cambios
            try {
                String nombre = txtNombre.getText();
                double precio = Double.parseDouble(txtPrecio.getText());
                int stock = txtStock.getText().isEmpty() ? 0 : Integer.parseInt(txtStock.getText());
                String descripcion = txtDescripcion.getText();
                String estado = chkEstado.getSelectedItem().toString();
                String nombreMarca = cboMarca.getSelectedItem().toString();

                int idMarca = objRepuesto.obtenerIdMarca(nombreMarca);

                objRepuesto.modificarRepuesto(idRepuestoSeleccionado, nombre, precio, stock, descripcion, estado, idMarca);

                JOptionPane.showMessageDialog(this, "Repuesto modificado correctamente.");
                listarRepuestos();
                habilitarControles(false); // Esto resetea los botones a "Nuevo" y "Modificar"
                limpiarControles();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error al modificar: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnModificarActionPerformed

