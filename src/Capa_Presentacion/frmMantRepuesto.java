    private void tblRepuestoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblRepuestoMouseClicked
        int fila = tblRepuesto.getSelectedRow();
        if (fila >= 0) {
            idRepuestoSeleccionado = (int) tblRepuesto.getValueAt(fila, 0);
            txtNombre.setText(tblRepuesto.getValueAt(fila, 1).toString());
            txtPrecio.setText(tblRepuesto.getValueAt(fila, 2).toString());
            txtStock.setText(tblRepuesto.getValueAt(fila, 3).toString());
            txtFechaRegistro.setText(tblRepuesto.getValueAt(fila, 4).toString());
            chkEstado.setSelectedItem(tblRepuesto.getValueAt(fila, 5).toString());

            Object desc = tblRepuesto.getValueAt(fila, 6);
            txtDescripcion.setText(desc != null ? desc.toString() : "");

            cboMarca.setSelectedItem(tblRepuesto.getValueAt(fila, 7).toString());
        }
    }//GEN-LAST:event_tblRepuestoMouseClicked

    private void chkEstadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chkEstadoActionPerformed
        // TODO add your handling code here:
        String estado = chkEstado.getSelectedItem().toString();
        if (estado.equalsIgnoreCase("agotado")) {
            txtStock.setText("0");
            txtStock.setEnabled(false); // Bloquea para que no escriban
        } else {
            // Solo desbloquear si estamos en modo edición (botones activos)
            if (btnNuevo.getText().equals("Guardar") || btnModificar.getText().equals("Guardar Cambios")) {
                txtStock.setEnabled(true);
            }
        }

    }//GEN-LAST:event_chkEstadoActionPerformed


