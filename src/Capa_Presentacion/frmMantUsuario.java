    public void mostrarUsuariosEnTabla(ResultSet rs) {
        try {
            Util.llenarTabla(tblUsuarios, rs);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "ERROR", JOptionPane.WARNING_MESSAGE);
        }
    }

    public void mostrarCantUsu() {
        try {
            int cantUsu = Usuario.contarUsuarios();
            lblNumUsu.setText(cantUsu + "");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "ERROR", JOptionPane.WARNING_MESSAGE);
        }
    }

