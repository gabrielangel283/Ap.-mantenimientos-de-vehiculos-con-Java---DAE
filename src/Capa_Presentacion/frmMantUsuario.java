    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int filaSelec = tblUsuarios.getSelectedRow();
        try {
            if (filaSelec != -1) {
                int usuario_id = (int) tblUsuarios.getValueAt(filaSelec, 0);
                int a = JOptionPane.showConfirmDialog(this, "¿Seguro que desea eliminar el usuario?", "Eliminar", JOptionPane.YES_NO_OPTION);
                if (a == 0) {
                    int result_eliminar = Usuario.eliminar(usuario_id);
                    switch (result_eliminar) {
                        case 1:
                            //Eliminacion logica - actualizar estado
                            JOptionPane.showMessageDialog(this, "Se actualizo el estado del usuario a inactivo. Eliminacion Logica");
                            break;
                        case 0:
                            //Eliminacion fisica - Borrar de la BD
                            JOptionPane.showMessageDialog(this, "Se ha borrado el usuario de la BD. Eliminacion fisica");
                            break;
                        default:
                            // Usuario ocupado - No se puede actualizar su estado
                            JOptionPane.showMessageDialog(this, "No se puede cambiar el estado de un usuario ocupado");
                            break;
                    }
                    mostrarUsuariosEnTabla(Usuario.listarUsuarios());
                    mostrarCantUsu();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Debe elegir un usuario para eliminar", "ELIMINAR", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "ERROR", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        int filaSelec = tblUsuarios.getSelectedRow();
        if (filaSelec != -1) {
            int usu_id = (int) tblUsuarios.getValueAt(filaSelec, 0);
            if (btnModificar.getText().equalsIgnoreCase("Modificar")) {
                habilitar(true);
                txtNombres.setText((String) tblUsuarios.getValueAt(filaSelec, 1));
                txtApellidos.setText((String) tblUsuarios.getValueAt(filaSelec, 2));
                txtDNI.setText((String) tblUsuarios.getValueAt(filaSelec, 3));
                txtTelefono.setText((String) tblUsuarios.getValueAt(filaSelec, 5));
                cboRol.setSelectedItem(tblUsuarios.getValueAt(filaSelec, 6).toString());
                txtClave.setEnabled(false);
                txtConfirmarClave.setEnabled(false);
                btnModificar.setText("Confirmar");
                btnModificar.setEnabled(true);
            } else { // Confirmar
                if (txtNombres.getText().trim().equals("") || txtApellidos.getText().trim().equals("") 
                        || txtDNI.getText().trim().equals("") || txtTelefono.getText().trim().equals("") 
                        || cboRol.getSelectedItem().toString().trim().equals("")) {
                    JOptionPane.showMessageDialog(this, "Debes ingresar los campos necesarios", "CAMPOS VACIOS", JOptionPane.INFORMATION_MESSAGE);
                    return;
                }
                try {
                    String nombres = txtNombres.getText();
                    String apellidos = txtApellidos.getText();
                    String dni = txtDNI.getText();
                    String rol = cboRol.getSelectedItem().toString().toLowerCase();
                    String telefono = txtTelefono.getText();

                    Usuario objUsu = new Usuario(nombres, apellidos, dni, null, telefono, rol);

                    Usuario.modificar(usu_id, objUsu);
                    JOptionPane.showMessageDialog(this, "Usuario modificado correctamente", "USUARIO REGISTRADO", JOptionPane.INFORMATION_MESSAGE);
                    mostrarUsuariosEnTabla(Usuario.listarUsuarios());
                    btnCancelarActionPerformed(null);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, e.getMessage(), "ERROR", JOptionPane.WARNING_MESSAGE);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Debe elegir un usuario para modificar");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

