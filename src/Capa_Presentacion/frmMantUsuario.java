    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        if (btnNuevo.getText().equalsIgnoreCase("Nuevo")) {
            habilitar(true);
            btnNuevo.setEnabled(true);
            btnNuevo.setText("Guardar");
        } else { //GUARDAR
            if (txtNombres.getText().trim().equals("") || txtClave.getText().trim().equals("")
                    || txtApellidos.getText().trim().equals("") || txtDNI.getText().trim().equals("")
                    || txtTelefono.getText().trim().equals("") || txtConfirmarClave.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(this, "Debes ingresar los campos necesarios");
                return;
            }
            try {
                String nombres = txtNombres.getText();
                String apellidos = txtApellidos.getText();
                String dni = txtDNI.getText();
                String clave = txtClave.getText();
                String rol = cboRol.getSelectedItem().toString().toLowerCase();
                String telefono = txtTelefono.getText();
                String claveConfir = txtConfirmarClave.getText();
                
                if(!clave.equals(claveConfir)) {
                    JOptionPane.showMessageDialog(this, "Las claves ingresadas deben ser iguales");
                    return;
                }

                Usuario objUsu = new Usuario(nombres, apellidos, dni, clave, telefono, rol);

                Usuario.registrar(objUsu);
                JOptionPane.showMessageDialog(this, "Usuario registrado correctamente");
                mostrarUsuariosEnTabla(Usuario.listarUsuarios());
                limpiarCampos();
                habilitar(false);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "ERROR", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnNuevoActionPerformed

    private void btnDarBajaAltaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDarBajaAltaActionPerformed
        int filaSelec = tblUsuarios.getSelectedRow();
        if (filaSelec != -1) {
            int usu_id = (int) tblUsuarios.getValueAt(filaSelec, 0);
            String usu_estado = (String) tblUsuarios.getValueAt(filaSelec, 7);
            try {
                if (usu_estado.equalsIgnoreCase("ocupado")) {
                    JOptionPane.showMessageDialog(this, "No de puede cambiar el estado de un usuario ocupado", "DAR BAJA/ALTA", HEIGHT, frameIcon);
                } else if (usu_estado.equalsIgnoreCase("disponible")) {
                    Usuario.darBaja(usu_id);
                    JOptionPane.showMessageDialog(this, "Usuario dado de baja");
                } else {
                    JOptionPane.showMessageDialog(this, "Usuario dado de alta");
                }
                mostrarUsuariosEnTabla(Usuario.listarUsuarios());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "ERROR", JOptionPane.WARNING_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(this, "Debe elegir a un usuario para dar de baja");
        }
    }//GEN-LAST:event_btnDarBajaAltaActionPerformed

