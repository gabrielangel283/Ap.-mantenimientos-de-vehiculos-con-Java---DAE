    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String nombre_bq = txtBusquedaNombre.getText();
        String Rol_bq = cboBusquedaRol.getSelectedItem().toString();
        
        if(nombre_bq.trim().equals("") && cboBusquedaRol.getSelectedItem().toString().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un nombre o sleeccionar el rol para buscar un usuario");
            return;
        }
        
        try {
            ResultSet rs = Usuario.buscarUsuarioPorNombreRol(nombre_bq, Rol_bq);
            mostrarUsuariosEnTabla(rs);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "ERROR", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed


